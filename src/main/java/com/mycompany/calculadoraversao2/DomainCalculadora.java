package com.mycompany.calculadoraversao2;

public class DomainCalculadora {

    public int soma(int a, int b) {
        return (a + b);
    }

    public int sub(int a, int b) {
        return (a - b);
    }

    public int divisao(int a, int b) {
        return (a / b);
    }

    public int multiplicacao(int a, int b) {
        return (a * b);
    }
}
